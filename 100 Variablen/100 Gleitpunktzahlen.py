# Zuweisung zu Variablen, zu beachten ist, daß als Trennzeichen
# ein Punkt und nicht ein Komma verwendet wird
x = 12.98
print(x)

# auch bei Gleitkommazahlen können Unterstriche für die Gruppierung verwendet werden
x = 3.000_000_1
print(x)

# Gleitpunktzahlen können auch in Exponential-Schreibweisen angegeben werden
x = 3.19e-3
print(x)

